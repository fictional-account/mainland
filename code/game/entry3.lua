function othershore_dsc(txt)
  return function()
    othershore._acts = othershore._acts + 1;
    local ret = txt;
    if othershore._acts > 4 then
      ret = ret .. othershore_timetogo2;
    elseif othershore._acts > 3 then
      ret = ret .. othershore_timetogo;
    end
    return ret;
  end
end

